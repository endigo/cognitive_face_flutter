/// Supported face attribute types
enum FaceAttributeType {
  /// Analyses age
  age,

  /// Analyses gender
  gender,

  /// Analyses facial hair
  /// "facialHair"
  facialHair,

  /// Analyses whether is smiling
  smile,

  /// Analyses head pose
  /// "headPose"
  headPose,

  /// Analyses glasses type
  glasses,

  /// Analyses emotion type
  emotion,

  /// Analyses hair type
  hair,

  /// Analyses makeup type
  makeup,

  /// Analyses occlusion type
  occlusion,

  /// Analyses accessories type
  accessories,

  /// Analyses noise type
  noise,

  /// Analyses exposure type
  exposure,

  /// Analyses blur type
  blur
}
